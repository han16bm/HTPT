-- 1. BỔ SUNG CÁC BẢNG LƯU TRỮ MỚI (PERMISSIONS, ROLE_PERMISSIONS, SYSTEM_SETTINGS, WHITELIST_ENTRIES, AUDIT_LOGS)

CREATE TABLE PERMISSIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PERM_CODE VARCHAR2(100 CHAR) NOT NULL,
    PERM_NAME VARCHAR2(200 CHAR) NOT NULL,
    MODULE VARCHAR2(50 CHAR) NOT NULL,
    DESCRIPTION CLOB,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UQ_PERM_CODE UNIQUE (PERM_CODE)
);

CREATE TABLE ROLE_PERMISSIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE_ID NUMBER NOT NULL,
    PERM_CODE VARCHAR2(100 CHAR) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_ROLE_PERM_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ID),
    CONSTRAINT UQ_ROLE_PERM UNIQUE (ROLE_ID, PERM_CODE)
);

CREATE TABLE SYSTEM_SETTINGS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SETTING_KEY VARCHAR2(100 CHAR) NOT NULL,
    SETTING_GROUP VARCHAR2(50 CHAR) NOT NULL,
    VALUE CLOB,
    DATA_TYPE VARCHAR2(20 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    UPDATED_BY VARCHAR2(100 CHAR),
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT UQ_SYS_SETTING_KEY UNIQUE (SETTING_KEY)
);

CREATE TABLE WHITELIST_ENTRIES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    RECORD_TYPE VARCHAR2(20 CHAR) NOT NULL,
    VALUE VARCHAR2(500 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
    IS_ENABLED NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_BY VARCHAR2(100 CHAR),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP
);

CREATE TABLE AUDIT_LOGS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACTION VARCHAR2(100 CHAR) NOT NULL,
    TARGET_TYPE VARCHAR2(50 CHAR) NOT NULL,
    TARGET_ID VARCHAR2(100 CHAR),
    OLD_VALUE CLOB,
    NEW_VALUE CLOB,
    PERFORMED_BY VARCHAR2(100 CHAR) NOT NULL,
    PERFORMED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    IP_ADDRESS VARCHAR2(50 CHAR),
    USER_AGENT VARCHAR2(500 CHAR)
);

