-- FISH_SHOP Oracle schema
-- Target: Oracle 19c+
-- Notes:
-- 1. Run with a user that can create tables, indexes, triggers, and sequences.
-- 2. Table names are uppercase to match common Oracle conventions.
-- 3. This schema replaces the old DM_LOAI_CA design.

CREATE TABLE ROLES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CODE VARCHAR2(30 CHAR) NOT NULL,
    NAME VARCHAR2(100 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UQ_ROLES_CODE UNIQUE (CODE)
);

CREATE TABLE USERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ROLE_ID NUMBER NOT NULL,
    USERNAME VARCHAR2(50 CHAR) NOT NULL,
    EMAIL VARCHAR2(150 CHAR),
    PASSWORD_HASH VARCHAR2(255 CHAR) NOT NULL,
    FULL_NAME VARCHAR2(150 CHAR),
    PHONE VARCHAR2(20 CHAR),
    AVATAR_URL VARCHAR2(500 CHAR),
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    LAST_LOGIN_AT DATE,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_USERS_ROLE FOREIGN KEY (ROLE_ID) REFERENCES ROLES(ID),
    CONSTRAINT UQ_USERS_USERNAME UNIQUE (USERNAME),
    CONSTRAINT UQ_USERS_EMAIL UNIQUE (EMAIL),
    CONSTRAINT UQ_USERS_PHONE UNIQUE (PHONE)
);

CREATE TABLE CUSTOMER_PROFILES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_ID NUMBER,
    CUSTOMER_CODE VARCHAR2(30 CHAR) NOT NULL,
    FULL_NAME VARCHAR2(150 CHAR) NOT NULL,
    PHONE VARCHAR2(20 CHAR) NOT NULL,
    EMAIL VARCHAR2(150 CHAR),
    DATE_OF_BIRTH DATE,
    GENDER VARCHAR2(10 CHAR),
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_CUSTOMER_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT UQ_CUSTOMER_CODE UNIQUE (CUSTOMER_CODE),
    CONSTRAINT UQ_CUSTOMER_USER UNIQUE (USER_ID),
    CONSTRAINT UQ_CUSTOMER_PHONE UNIQUE (PHONE)
);

CREATE TABLE CUSTOMER_ADDRESSES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID NUMBER NOT NULL,
    RECEIVER_NAME VARCHAR2(150 CHAR) NOT NULL,
    RECEIVER_PHONE VARCHAR2(20 CHAR) NOT NULL,
    PROVINCE VARCHAR2(100 CHAR) NOT NULL,
    DISTRICT VARCHAR2(100 CHAR) NOT NULL,
    WARD VARCHAR2(100 CHAR),
    ADDRESS_LINE VARCHAR2(255 CHAR) NOT NULL,
    IS_DEFAULT NUMBER(1) DEFAULT 0 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_ADDRESS_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER_PROFILES(ID)
);

CREATE TABLE CATEGORIES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PARENT_ID NUMBER,
    CATEGORY_CODE VARCHAR2(30 CHAR) NOT NULL,
    NAME VARCHAR2(150 CHAR) NOT NULL,
    SLUG VARCHAR2(200 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(1000 CHAR),
    IMAGE_URL VARCHAR2(500 CHAR),
    DISPLAY_ORDER NUMBER DEFAULT 0 NOT NULL,
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_CATEGORY_PARENT FOREIGN KEY (PARENT_ID) REFERENCES CATEGORIES(ID),
    CONSTRAINT UQ_CATEGORY_CODE UNIQUE (CATEGORY_CODE),
    CONSTRAINT UQ_CATEGORY_SLUG UNIQUE (SLUG)
);

CREATE TABLE PRODUCTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CATEGORY_ID NUMBER NOT NULL,
    PRODUCT_CODE VARCHAR2(30 CHAR) NOT NULL,
    SKU VARCHAR2(50 CHAR),
    NAME VARCHAR2(200 CHAR) NOT NULL,
    SLUG VARCHAR2(250 CHAR) NOT NULL,
    SHORT_DESCRIPTION VARCHAR2(500 CHAR),
    DESCRIPTION CLOB,
    IMAGE_URL VARCHAR2(500 CHAR),
    COST_PRICE NUMBER(14,2) DEFAULT 0 NOT NULL,
    SALE_PRICE NUMBER(14,2) NOT NULL,
    STOCK_QUANTITY NUMBER DEFAULT 0 NOT NULL,
    SOLD_QUANTITY NUMBER DEFAULT 0 NOT NULL,
    WEIGHT_GRAMS NUMBER(10,2),
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    IS_FEATURED NUMBER(1) DEFAULT 0 NOT NULL,
    CREATED_BY NUMBER,
    UPDATED_BY NUMBER,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_PRODUCT_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES(ID),
    CONSTRAINT FK_PRODUCT_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES USERS(ID),
    CONSTRAINT FK_PRODUCT_UPDATED_BY FOREIGN KEY (UPDATED_BY) REFERENCES USERS(ID),
    CONSTRAINT UQ_PRODUCT_CODE UNIQUE (PRODUCT_CODE),
    CONSTRAINT UQ_PRODUCT_SKU UNIQUE (SKU),
    CONSTRAINT UQ_PRODUCT_SLUG UNIQUE (SLUG)
);

CREATE TABLE PRODUCT_IMAGES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER NOT NULL,
    IMAGE_URL VARCHAR2(500 CHAR) NOT NULL,
    ALT_TEXT VARCHAR2(255 CHAR),
    IS_PRIMARY NUMBER(1) DEFAULT 0 NOT NULL,
    DISPLAY_ORDER NUMBER DEFAULT 0 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_PRODUCT_IMAGE_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID)
);

CREATE TABLE INVENTORY_TRANSACTIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PRODUCT_ID NUMBER NOT NULL,
    TRANSACTION_TYPE VARCHAR2(20 CHAR) NOT NULL,
    QUANTITY NUMBER NOT NULL,
    UNIT_COST NUMBER(14,2),
    REFERENCE_TYPE VARCHAR2(30 CHAR),
    REFERENCE_ID NUMBER,
    NOTE VARCHAR2(500 CHAR),
    CREATED_BY NUMBER,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_INV_TX_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID),
    CONSTRAINT FK_INV_TX_USER FOREIGN KEY (CREATED_BY) REFERENCES USERS(ID),
    CONSTRAINT CK_INV_TX_TYPE CHECK (TRANSACTION_TYPE IN ('IMPORT', 'EXPORT', 'ADJUSTMENT', 'SALE', 'RETURN'))
);

CREATE TABLE PROMOTIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PROMO_CODE VARCHAR2(30 CHAR) NOT NULL,
    TITLE VARCHAR2(200 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(1000 CHAR),
    DISCOUNT_TYPE VARCHAR2(20 CHAR) NOT NULL,
    DISCOUNT_VALUE NUMBER(14,2) NOT NULL,
    MAX_DISCOUNT_VALUE NUMBER(14,2),
    MIN_ORDER_VALUE NUMBER(14,2) DEFAULT 0 NOT NULL,
    START_AT DATE NOT NULL,
    END_AT DATE NOT NULL,
    USAGE_LIMIT NUMBER,
    USED_COUNT NUMBER DEFAULT 0 NOT NULL,
    STATUS NUMBER(1) DEFAULT 1 NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UQ_PROMOTIONS_CODE UNIQUE (PROMO_CODE),
    CONSTRAINT CK_PROMO_TYPE CHECK (DISCOUNT_TYPE IN ('PERCENT', 'AMOUNT'))
);

CREATE TABLE PROMOTION_PRODUCTS (
    PROMOTION_ID NUMBER NOT NULL,
    PRODUCT_ID NUMBER NOT NULL,
    PRIMARY KEY (PROMOTION_ID, PRODUCT_ID),
    CONSTRAINT FK_PROMO_PRODUCT_PROMO FOREIGN KEY (PROMOTION_ID) REFERENCES PROMOTIONS(ID),
    CONSTRAINT FK_PROMO_PRODUCT_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID)
);

CREATE TABLE SHOPPING_CARTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID NUMBER NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'ACTIVE' NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_CART_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER_PROFILES(ID),
    CONSTRAINT UQ_CART_CUSTOMER_STATUS UNIQUE (CUSTOMER_ID, STATUS)
);

CREATE TABLE CART_ITEMS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CART_ID NUMBER NOT NULL,
    PRODUCT_ID NUMBER NOT NULL,
    QUANTITY NUMBER NOT NULL,
    UNIT_PRICE NUMBER(14,2) NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_CART_ITEM_CART FOREIGN KEY (CART_ID) REFERENCES SHOPPING_CARTS(ID),
    CONSTRAINT FK_CART_ITEM_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID),
    CONSTRAINT UQ_CART_ITEM UNIQUE (CART_ID, PRODUCT_ID),
    CONSTRAINT CK_CART_ITEM_QTY CHECK (QUANTITY > 0)
);

CREATE TABLE ORDERS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_CODE VARCHAR2(30 CHAR) NOT NULL,
    CUSTOMER_ID NUMBER,
    ADDRESS_ID NUMBER,
    PROMOTION_ID NUMBER,
    ORDER_SOURCE VARCHAR2(20 CHAR) DEFAULT 'ONLINE' NOT NULL,
    ORDER_STATUS VARCHAR2(20 CHAR) DEFAULT 'PENDING' NOT NULL,
    PAYMENT_STATUS VARCHAR2(20 CHAR) DEFAULT 'UNPAID' NOT NULL,
    PAYMENT_METHOD VARCHAR2(20 CHAR) DEFAULT 'COD' NOT NULL,
    CUSTOMER_NAME VARCHAR2(150 CHAR) NOT NULL,
    CUSTOMER_PHONE VARCHAR2(20 CHAR) NOT NULL,
    CUSTOMER_EMAIL VARCHAR2(150 CHAR),
    CUSTOMER_ADDRESS VARCHAR2(500 CHAR) NOT NULL,
    NOTE VARCHAR2(1000 CHAR),
    SUBTOTAL_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    DISCOUNT_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    SHIPPING_FEE NUMBER(14,2) DEFAULT 0 NOT NULL,
    TOTAL_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    CREATED_BY NUMBER,
    CONFIRMED_AT DATE,
    SHIPPED_AT DATE,
    DELIVERED_AT DATE,
    CANCELLED_AT DATE,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_ORDER_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER_PROFILES(ID),
    CONSTRAINT FK_ORDER_ADDRESS FOREIGN KEY (ADDRESS_ID) REFERENCES CUSTOMER_ADDRESSES(ID),
    CONSTRAINT FK_ORDER_PROMOTION FOREIGN KEY (PROMOTION_ID) REFERENCES PROMOTIONS(ID),
    CONSTRAINT FK_ORDER_CREATED_BY FOREIGN KEY (CREATED_BY) REFERENCES USERS(ID),
    CONSTRAINT UQ_ORDER_CODE UNIQUE (ORDER_CODE),
    CONSTRAINT CK_ORDER_SOURCE CHECK (ORDER_SOURCE IN ('ONLINE', 'POS')),
    CONSTRAINT CK_ORDER_STATUS CHECK (ORDER_STATUS IN ('PENDING', 'CONFIRMED', 'SHIPPING', 'COMPLETED', 'CANCELLED')),
    CONSTRAINT CK_PAYMENT_STATUS CHECK (PAYMENT_STATUS IN ('UNPAID', 'PARTIAL', 'PAID', 'REFUNDED')),
    CONSTRAINT CK_PAYMENT_METHOD CHECK (PAYMENT_METHOD IN ('COD', 'BANK_TRANSFER', 'CASH'))
);

CREATE TABLE ORDER_ITEMS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER NOT NULL,
    PRODUCT_ID NUMBER NOT NULL,
    PRODUCT_NAME VARCHAR2(200 CHAR) NOT NULL,
    SKU VARCHAR2(50 CHAR),
    QUANTITY NUMBER NOT NULL,
    UNIT_PRICE NUMBER(14,2) NOT NULL,
    DISCOUNT_AMOUNT NUMBER(14,2) DEFAULT 0 NOT NULL,
    LINE_TOTAL NUMBER(14,2) NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_ORDER_ITEM_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ID),
    CONSTRAINT FK_ORDER_ITEM_PRODUCT FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCTS(ID),
    CONSTRAINT CK_ORDER_ITEM_QTY CHECK (QUANTITY > 0)
);

CREATE TABLE PAYMENTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ORDER_ID NUMBER NOT NULL,
    PAYMENT_CODE VARCHAR2(30 CHAR) NOT NULL,
    METHOD VARCHAR2(20 CHAR) NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'PENDING' NOT NULL,
    AMOUNT NUMBER(14,2) NOT NULL,
    TRANSACTION_REF VARCHAR2(100 CHAR),
    PAID_AT DATE,
    NOTE VARCHAR2(500 CHAR),
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_PAYMENT_ORDER FOREIGN KEY (ORDER_ID) REFERENCES ORDERS(ID),
    CONSTRAINT UQ_PAYMENT_CODE UNIQUE (PAYMENT_CODE),
    CONSTRAINT CK_PAYMENT_METHOD2 CHECK (METHOD IN ('COD', 'BANK_TRANSFER', 'CASH')),
    CONSTRAINT CK_PAYMENT_STATUS2 CHECK (STATUS IN ('PENDING', 'SUCCESS', 'FAILED', 'REFUNDED'))
);

CREATE TABLE BLOG_CATEGORIES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NAME VARCHAR2(150 CHAR) NOT NULL,
    SLUG VARCHAR2(200 CHAR) NOT NULL,
    DESCRIPTION VARCHAR2(500 CHAR),
