    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UQ_BLOG_CATEGORY_SLUG UNIQUE (SLUG)
);

CREATE TABLE BLOG_POSTS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CATEGORY_ID NUMBER,
    AUTHOR_ID NUMBER,
    TITLE VARCHAR2(250 CHAR) NOT NULL,
    SLUG VARCHAR2(300 CHAR) NOT NULL,
    SUMMARY VARCHAR2(1000 CHAR),
    CONTENT CLOB NOT NULL,
    THUMBNAIL_URL VARCHAR2(500 CHAR),
    STATUS VARCHAR2(20 CHAR) DEFAULT 'DRAFT' NOT NULL,
    PUBLISHED_AT DATE,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT FK_BLOG_POST_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES BLOG_CATEGORIES(ID),
    CONSTRAINT FK_BLOG_POST_AUTHOR FOREIGN KEY (AUTHOR_ID) REFERENCES USERS(ID),
    CONSTRAINT UQ_BLOG_POST_SLUG UNIQUE (SLUG),
    CONSTRAINT CK_BLOG_POST_STATUS CHECK (STATUS IN ('DRAFT', 'PUBLISHED', 'HIDDEN'))
);

CREATE TABLE CONTACT_MESSAGES (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FULL_NAME VARCHAR2(150 CHAR) NOT NULL,
    EMAIL VARCHAR2(150 CHAR),
    PHONE VARCHAR2(20 CHAR),
    SUBJECT VARCHAR2(200 CHAR),
    MESSAGE CLOB NOT NULL,
    STATUS VARCHAR2(20 CHAR) DEFAULT 'NEW' NOT NULL,
    CREATED_AT DATE DEFAULT SYSDATE NOT NULL,
    UPDATED_AT DATE DEFAULT SYSDATE NOT NULL,
    CONSTRAINT CK_CONTACT_STATUS CHECK (STATUS IN ('NEW', 'READ', 'RESOLVED', 'CLOSED'))
);

