    ID               DECIMAL(18,0) IDENTITY(1,1) PRIMARY KEY,
    ORDER_CODE       VARCHAR(30)   NOT NULL UNIQUE,
    CUSTOMER_ID      DECIMAL(18,0) NULL,
    CUSTOMER_NAME    NVARCHAR(150) NULL,
    CUSTOMER_PHONE   VARCHAR(20)   NULL,
    CUSTOMER_EMAIL   VARCHAR(150)  NULL,
    CUSTOMER_ADDRESS NVARCHAR(500) NULL,
    ADDRESS_ID       DECIMAL(18,0) NULL,
    SUBTOTAL_AMOUNT  DECIMAL(18,2) DEFAULT 0,
    DISCOUNT_AMOUNT  DECIMAL(18,2) DEFAULT 0,
    SHIPPING_FEE     DECIMAL(18,2) DEFAULT 0,
    TOTAL_AMOUNT     DECIMAL(18,2) DEFAULT 0,
    PROMOTION_ID     DECIMAL(18,0) NULL,
    PAYMENT_METHOD   VARCHAR(20)   DEFAULT 'COD',
    PAYMENT_STATUS   VARCHAR(20)   DEFAULT 'UNPAID',
    ORDER_STATUS     VARCHAR(20)   DEFAULT 'PENDING',
    ORDER_SOURCE     VARCHAR(20)   DEFAULT 'ONLINE',
    NOTE             NVARCHAR(1000) NULL,
    CONFIRMED_AT     DATETIME2     NULL,
    SHIPPED_AT       DATETIME2     NULL,
    DELIVERED_AT     DATETIME2     NULL,
    CANCELLED_AT     DATETIME2     NULL,
    CREATED_BY       DECIMAL(18,0) NULL,
    CREATED_AT       DATETIME2     DEFAULT SYSUTCDATETIME(),
